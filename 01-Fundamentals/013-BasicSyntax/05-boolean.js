// Basic Booleans
// The value of a boolean is either true or false.

let isWomen = true;
let isMen = false;

// Output: true
console.log(isWomen);
// Output: false
console.log(isMen);

// Boolean Operations

// Comparison Operators
let x = 10;
let y = 8;

// Output: true
console.log(x > y);
// Output: false
console.log(x < y);
// Output: false
console.log(x == y);
// Output: true
console.log(x != y);
// Output: true
console.log(x >= y);
// Output: false
console.log(x <= y);

// Logical Operators
let p = true;
let q = false;

// Output: true
console.log(p && q);
// Output: true
console.log(p || q);
// Output: false
console.log(!p);

