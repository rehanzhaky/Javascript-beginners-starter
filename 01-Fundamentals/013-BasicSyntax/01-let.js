// Use let when u know the value of the variable might change during the execution of ur program.
// Declares a variable name "age"
let age; 
// Assigns a value to the variable
age = 23;
// Prints the value of the variable
console.log(age);

// Year Changes
// Reassigns a value to the variable
age = 24;
// Prints the value of the variable
console.log(age);
